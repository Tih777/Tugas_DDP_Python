#Bualah program untuk menghitung konversi suhu celcius ke fahreinhet ke reamur

#Input
c=input('Masukan nilai suhu dalam celcius = ')

#rumus
fah = int(c) * 9/5 + 32
rea = 4/5 * int(c)


#output
print()
print("Hasil dalam fahrenheit adalah = ",fah)
print("Hasil daalam reamur adalah = ",rea)
print()