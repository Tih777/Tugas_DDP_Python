# Rumus luas bangun datar

def segitiga(a,t):
    b = 0.5
    s = b * a * t
    print('Luas segitiga tersebut adalah: ',s)

def lingkaran(r):
    p = 3.14
    l = p * r * r
    print('Luas lingkaran tersebut adalah: ',l)

def persegi(s):
    p = s * s
    print('Luas persegi tersebut: ',p)

def persegi_panjang(p,l):
    lp = p * l
    print('Luas persegi panjang tersebut: ',lp)

def belah_ketupat(d1,d2):
    lb = d1 * d2 / 2
    print('Luas belah ketupat tersebut adalah: ',lb)

